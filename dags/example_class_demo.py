from datetime import datetime

from airflow.sdk import dag, task


@dag(
    dag_id="class_demo_hello",
    schedule="@daily",
    start_date=datetime(2026, 1, 1),
    catchup=False,
    tags=["class-demo", "teacher", "week11"],
)
def class_demo_hello():
    @task()
    def greet() -> str:
        return "hello from the shared class Airflow"

    @task()
    def announce(message: str) -> None:
        print(f"[class-demo] {message}")

    announce(greet())


class_demo_hello()
